    struct StereoPair final
    {
      short left = 0;
      short right = 0;

      StereoPair() = default;
      StereoPair(short l, short r) : left(l), right(r) {}

      void mul(int fixed_16_left, int fixed_16_right)
      {
        left = short((int(left) * fixed_16_left) >> 16);
        right = short((int(right) * fixed_16_right) >> 16);
      }

      void mul(int fixed_16) { mul(fixed_16, fixed_16); }

      StereoPair operator+(const StereoPair& other) const
      {
        return StereoPair(std::clamp(int(left) + other.left, -0x7fff, 0x7fff),
                          std::clamp(int(right) + other.right, -0x7fff, 0x7fff));
      }

      static StereoPair interp(const StereoPair& lhs, const StereoPair& rhs, int fixed_16)
      {
        return StereoPair(short(lhs.left + (((rhs.left - lhs.left) * fixed_16) >> 16)),
                          short(lhs.right + (((rhs.right - lhs.right) * fixed_16) >> 16)));
      }
    };

    using GranualarBuffer =
        std::array<StereoPair, sizeof(GRANUALAR_WINDOW) / sizeof(GRANUALAR_WINDOW[0])>;
    static constexpr int GRANULAR_BUFFER_BITS =
        32 - __builtin_ctz(sizeof(GRANUALAR_WINDOW) / sizeof(GRANUALAR_WINDOW[0]));

    class Granual final
    {
    private:
      GranualarBuffer m_buffer{};

    public:
      Granual() = default;
      Granual(const Granual&) = default;
      Granual& operator=(const Granual&) = default;
      Granual(Granual&&) = default;

      Granual(const GranualarBuffer& input, bool split)
      {
        if (split)
        {
          auto buffer_middle = m_buffer.begin() + m_buffer.size() / 2;
          auto input_middle = input.begin() + input.size() / 2;
          std::copy(input.begin(), input_middle, buffer_middle);
          std::copy(input_middle, input.end(), m_buffer.begin());
        }
        else
        {
          std::copy(input.begin(), input.end(), m_buffer.begin());
        }

        for (size_t i = 0; i < m_buffer.size(); ++i)
        {
          m_buffer[i].mul(GRANUALAR_WINDOW[i]);
        }
      }

      StereoPair operator[](uint32_t frac) const
      {
        int index = frac >> GRANULAR_BUFFER_BITS;

        StereoPair s0 = m_buffer[(index + 0) & (m_buffer.size() - 1)];
        StereoPair s1 = m_buffer[(index + 1) & (m_buffer.size() - 1)];

        int w = 0xffff & (frac >> (GRANULAR_BUFFER_BITS - 16));

        return StereoPair::interp(s0, s1, w);
      }
    };

